#sort() 永久性修改  sorted()临时修改  reverse()倒序,forever
cars_name= ['tesla','bmw','audi','toyota','subaru']
cars_name.sort()
print(cars_name)

cars_name.sort(reverse=True)
print(cars_name)

print(sorted(cars_name))
print(sorted(cars_name,reverse=True))
print(cars_name)

cars_name.reverse()
print(cars_name)

